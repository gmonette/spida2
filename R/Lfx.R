#' @describeIn M '<' method for factors
#' @export
"<.factor" <- function(x,y) {
  if ( ! identical( levels(x), levels(y))) warning( "Different 'levels' in x and y")
  as.numeric(x) < as.numeric(y)
}

#' @describeIn M '>' method for factors
#' @export
">.factor" <- function(x,y) {
  if ( ! identical( levels(x), levels(y))) warning( "Different 'levels' in x and y")
  as.numeric(x) > as.numeric(y)
}
#' @describeIn M '<=' method for factors
#' @export
"<=.factor" <- function(x,y) {
  if ( ! identical( levels(x), levels(y))) warning( "Different 'levels' in x and y")
  as.numeric(x) <= as.numeric(y)
}

#' @export
#' @describeIn M '>=' method for factors
">=.factor" <- function(x,y) {
  if ( ! identical( levels(x), levels(y))) warning( "Different 'levels' in x and y")
  as.numeric(x) >= as.numeric(y)
}
